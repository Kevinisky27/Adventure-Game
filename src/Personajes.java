public class Personajes {
    private int NiveldeEnergia;
    private int NumerodeVida;
    private int CapOfensiva;

    public int getNiveldeEnergia() {
        return NiveldeEnergia;
    }

    public void setNiveldeEnergia(int NiveldeEnergia) {
        this.NiveldeEnergia = NiveldeEnergia;
    }

    public int getNumerodeVida() {
        return NumerodeVida;
    }

    public void setNumerodeVida(int NumerodeVida) {
        this.NumerodeVida = NumerodeVida;
    }

    public int getCapOfensiva() {
        return CapOfensiva;
    }

    public void setCapOfensiva(int CapacidadOfensiva) {
        this.CapOfensiva = CapacidadOfensiva;
    }
}
